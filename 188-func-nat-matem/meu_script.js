//ceil arredonda sempre para o valor inteiro acima do existente
var x = Math.ceil(10.380)

document.write(x)

document.write('<br/><br/>')

//floor desconsidera o conteúdo fracionado e devolve apenas a parte inteira do valor
var y = Math.floor(10.380)

document.write(y)

document.write('<br/><br/>')

//round leva em consideração o peso da fração e arredonda pra cima ou pra baixo
var z = Math.round(10.480)

document.write(z)

document.write('<br/><br/>')

//devolve um valor randomico entre 0 e 1
var a = Math.random()

document.write(a)
